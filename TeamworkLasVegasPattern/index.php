                <h5>Located on the Las Vegas Strip just south of Russell Road, this historic sign famously reads, "Welcome to Fabulous Las Vegas, Nevada" on the front and "Drive Carefully" and "Come Back Soon" on the back. The sign was designed by Betty Willis while she worked for Western Neon Company and cost $4,000 to build and install. Willis also designed the iconic signs for the Moulin Rouge hotel.
                    When it was created in 1959, access to the sign was very limited -- the median's curbs (along with the lack of close parking) made it challenging to stand right next to one of city's oldest symbols. The result? People either jaywalked, or risked parking in one of the Las Vegas travel lanes.
                    But scurrying across the street and dodging speeding cars is now a thing of the past. You and your group can now pose by the sign and snap away with ease. The iconic "Welcome to Las Vegas" sign features a 12-car parking lot, which is designed to improve pedestrian safety and even provide access for the disabled. There is also enough space to park two buses.
                    Another sign of progress for the iconic sign -- it is now solar powered. The city recently installed power trees on the signs west side. While the trees might temporarily block your view while driving by, you can easily park and get an unobstructed photo in front of it.
                    The sign's original creation was inspired by her father, Las Vegas pioneer S.R. Whitehead, Clark County's first tax assessor, who died 18 years before the sign went up.
                    "Dad was so strong on getting people to come here," Willis told the Las Vegas Sun in 2005. "I think my dad would be proud of what the sign has accomplished."
                    On May 1, 2009, the "Welcome to Las Vegas" sign was added to the National Register of Historic Places.</h5>
